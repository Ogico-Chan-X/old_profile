$(".colors1").waypoint({
  handler(direction) {
    if (direction === 'down') {
      $('body').removeClass("colorsForSec0")
               .removeClass("colorsForSec1")
               .removeClass("colorsForSec2")
               .removeClass("colorsForSec3")
               .removeClass("colorsForSec4")
               .removeClass("colorsForSec5")
               .addClass("colorsForSec1");
    }
    
    if (direction === 'up') {
      $('body').removeClass("colorsForSec0")
               .removeClass("colorsForSec1")
               .removeClass("colorsForSec2")
               .removeClass("colorsForSec3")
               .removeClass("colorsForSec4")
               .removeClass("colorsForSec5")
               .addClass("colorsForSec0");
    }
  },
  offset: '50%',
});


